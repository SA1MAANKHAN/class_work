public class incrementOperator {
    public static void main(String args[])
    {
        int m = 2, n = 3;
        System.out.println(" m = " + m);
        System.out.println(" n = " + n);
        System.out.println(" ++m = " + ++m);
        System.out.println(" n++ = " + n++);
        System.out.println(" m = " + m);
        System.out.println(" n = " + n);
    }
}
